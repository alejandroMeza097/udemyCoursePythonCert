'''
Aprendiendo sobre las funciones int() y float()
'''
numero_entero : int = 190
numero_flotante : float =  12.3456
print(f'El numero entero es : {numero_entero}')
print(f'El numero flotante es : {numero_flotante}')
print(f'{numero_flotante} es de tipo {type(numero_flotante)}')
print(f'{numero_entero} es de tipo {type(numero_entero)}')
print(int(numero_flotante))
print(float(numero_entero))
